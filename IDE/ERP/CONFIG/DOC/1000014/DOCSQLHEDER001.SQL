SELECT J4.ID AS DS$ID 
      ,J4.FLAG_DELETE, J4.FLAG_COMMIT, J4.FLAG_LOCK
      ,J4.ID, J4.TYPE_ID, TD.NAME AS TYPE_ID_ , TD.COLOR_FRG ,TD.COLOR_BGR
      ,J4.DOCNUMBER , J4.DOCNUMBERSTR, J4.DOCNUMBERIN
      ,J4.DOCSUMVAL , J4.DOCSUM, J4.NAME
      ,J4.DATE_COMMIT, J4.DATE_IN, J4.DOCSTR
      ,J4.FIRM_ID     , FR.NAME  AS FIRM_ID_
      ,J4.FILIAL_ID   , FL.NAME  AS FILIAL_ID_
      ,J4.USER_ID     , COALESCE(WZ.NAME, J4.USER_ID) AS WRK_NAME
      ,J4.VALUTE_ID   , C.VALUTE_SHORT AS VALUTE_ID_
      ,J14.CS_ID      , CS.NAME  AS CS_ID_
      ,J14.CS_ADDR_ID
      ,J14.PLACE_ID   , PL.NAME  AS PLACE_ID_
      ,J14.TO_PLACE_ID, PLT.NAME AS TO_PLACE_ID_
      ,J14.TAX_ID     , TAX.NAME AS TAX_ID_
      ,J14.WRK_ID     , WRK.NAME AS WRK_ID_
      ,J14.TMC_GRID_ID, TGR.NAME AS TMC_GRID_ID_
      ,J14.P_SUM, J14.P_NDP, J14.P_SUMNDP
      ,J14.J_PAY_ID
      ,(SELECT FIRST 1 T0010.DOCSTR FROM TABL$J_4 T0010 WHERE (T0010.ID = J14.J_PAY_ID)) AS J_PAY_ID_
FROM   TABL$J_4 J4
          LEFT OUTER JOIN TABL$R_FIRMS     FR  ON (FR.ID      = J4.FIRM_ID)
          LEFT OUTER JOIN TABL$R_FILIALS   FL  ON (FL.ID      = J4.FILIAL_ID)
          LEFT OUTER JOIN TABL$_TB_DOCS    TD  ON (TD.TYPE_ID = J4.TYPE_ID)
          LEFT OUTER JOIN TABL$R_COUNTRIES C   ON (C.ID       = J4.VALUTE_ID)
          LEFT OUTER JOIN TABL$R_WRK       WZ  ON (TRIM(WZ.USER_NAME) = J4.USER_ID)
          LEFT OUTER JOIN TABL$J_1000014   J14 ON (J14.J_ID   = J4.ID)
          LEFT OUTER JOIN TABL$R_CS        CS  ON (CS.ID      = J14.CS_ID)
          LEFT OUTER JOIN TABL$R_PLACES    PL  ON (PL.ID      = J14.PLACE_ID)
          LEFT OUTER JOIN TABL$R_PLACES    PLT ON (PLT.ID     = J14.TO_PLACE_ID)
          LEFT OUTER JOIN TABL$R_TAXES     TAX ON (TAX.ID     = J14.TAX_ID)
          LEFT OUTER JOIN TABL$R_TMC_GRID  TGR ON (TGR.ID     = J14.TMC_GRID_ID)
          LEFT OUTER JOIN TABL$R_WRK       WRK ON (WRK.ID     = J14.WRK_ID)
WHERE (J4.ID = ?J_ID)
ORDER BY DS$ID ASCENDING 
